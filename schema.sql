  return found;
end $$;

-- ── Edições ──────────────────────────────────────────────────────────────────
create or replace function public.edition_merge_field(edition_id uuid, field_name text, delta jsonb,
                                                       p_token text default null)
returns jsonb language plpgsql security definer set search_path to 'public' as $$
declare uid uuid; result jsonb; adm boolean;
begin
  uid := public.session_owner(p_token);
  if uid is null then raise exception 'sessão inválida'; end if;
  adm := public.is_admin(uid);
  if not adm and not public._edition_can_write(uid, field_name) then
    raise exception 'sem permissão para escrever em %', field_name;
  end if;
  update public.editions
     set data = jsonb_set(coalesce(data,'{}'::jsonb), array[field_name],
                          coalesce(data->field_name,'{}'::jsonb) || delta, true)
   where id = edition_id
   returning data into result;
  return result;
end $$;

create or replace function public.edition_append_array(edition_id uuid, field_name text, item jsonb,
                                                        p_token text default null)
returns jsonb language plpgsql security definer set search_path to 'public' as $$
declare uid uuid; result jsonb; current_arr jsonb; item_id text;
begin
  uid := public.session_owner(p_token);
  if uid is null then raise exception 'sessão inválida'; end if;
  if not public.is_admin(uid) and not public._edition_can_write(uid, field_name) then
    raise exception 'sem permissão'; end if;

  select coalesce(data->field_name, '[]'::jsonb) into current_arr
    from public.editions where id = edition_id;
  item_id := item->>'id';
  if item_id is not null and exists (
    select 1 from jsonb_array_elements(current_arr) elem where elem->>'id' = item_id
  ) then
    select data into result from public.editions where id = edition_id;
    return result;
  end if;
  update public.editions
     set data = jsonb_set(coalesce(data,'{}'::jsonb), array[field_name],
                          current_arr || jsonb_build_array(item), true)
   where id = edition_id
   returning data into result;
  return result;
end $$;

create or replace function public.edition_remove_from_array(edition_id uuid, field_name text, item_id text,
                                                            p_token text default null)
returns jsonb language plpgsql security definer set search_path to 'public' as $$
declare uid uuid; result jsonb; current_arr jsonb; filtered jsonb; owner text;
begin
  uid := public.session_owner(p_token);
  if uid is null then raise exception 'sessão inválida'; end if;

  select coalesce(data->field_name, '[]'::jsonb) into current_arr
    from public.editions where id = edition_id;

  select elem->>'by' into owner
    from jsonb_array_elements(current_arr) elem where elem->>'id' = item_id limit 1;
  if not public.is_admin(uid) and owner is distinct from uid::text then
    raise exception 'só podes remover o que é teu';
  end if;

  select coalesce(jsonb_agg(elem), '[]'::jsonb) into filtered
    from jsonb_array_elements(current_arr) elem
   where elem->>'id' is null or elem->>'id' <> item_id;

  update public.editions
     set data = jsonb_set(coalesce(data,'{}'::jsonb), array[field_name], filtered, true)
   where id = edition_id
   returning data into result;
  return result;
end $$;

create or replace function public.edition_write(p_token text, p_edition_id uuid, p_data jsonb default null,
                                                p_phase integer default null, p_status text default null,
                                                p_number integer default null, p_title text default null,
                                                p_subtitle text default null)
returns boolean language plpgsql security definer set search_path to 'public' as $$
declare uid uuid; adm boolean;
begin
  uid := public.session_owner(p_token);
  if uid is null then raise exception 'sessão inválida'; end if;
  adm := public.is_admin(uid);
  if not adm and (p_phase is not null or p_status is not null
                  or p_number is not null or p_title is not null or p_subtitle is not null) then
    raise exception 'só o admin muda a fase, o estado ou o nome';
  end if;
  update public.editions set
    data          = coalesce(p_data, data),
    current_phase = coalesce(p_phase, current_phase),
    status        = coalesce(p_status, status),
    number        = coalesce(p_number, number),
    title         = coalesce(p_title, title),
    subtitle      = coalesce(p_subtitle, subtitle)
  where id = p_edition_id;
  return found;
end $$;

create or replace function public.edition_create(p_token text, p_number integer, p_title text,
                                                 p_subtitle text, p_status text, p_data jsonb)
returns public.editions language plpgsql security definer set search_path to 'public' as $$
declare uid uuid; row public.editions;
begin
  uid := public.session_owner(p_token);
  if uid is null then raise exception 'sessão inválida'; end if;
  if p_status <> 'proposed' and not public.is_admin(uid) then
    raise exception 'só o admin cria aventuras'; end if;
  insert into public.editions(number, title, subtitle, status, current_phase, data)
  values (coalesce(p_number,0), coalesce(p_title,'Nova Aventura'),
          coalesce(p_subtitle,''), coalesce(p_status,'active'), 1, coalesce(p_data,'{}'::jsonb))
  returning * into row;
  return row;
end $$;

