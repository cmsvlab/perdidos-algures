-- Impede criar/escalar contas admin a partir da app.
create or replace function public.profiles_guard_admin()
returns trigger language plpgsql security definer set search_path to 'public' as $$
begin
  if tg_op = 'INSERT' then
    if coalesce(new.is_admin, false) then
      raise exception 'nao e permitido criar contas admin a partir da app';
    end if;
  elsif tg_op = 'UPDATE' then
    if coalesce(new.is_admin, false) is distinct from coalesce(old.is_admin, false) then
      raise exception 'nao e permitido alterar o estatuto de admin a partir da app';
    end if;
  end if;
  return new;
end;
$$;

drop trigger if exists profiles_guard_admin_trg on public.profiles;
create trigger profiles_guard_admin_trg
  before insert or update on public.profiles
  for each row execute function public.profiles_guard_admin();

-- Trigger opcional (Supabase Auth). A app usa auth próprio (profiles_auth), por
-- isso normalmente não dispara; incluído por completude.
create or replace function public.handle_new_user()
returns trigger language plpgsql security definer set search_path to 'public' as $$
declare uname text;
begin
  uname := coalesce(new.raw_user_meta_data->>'name', split_part(new.email,'@',1));
  insert into public.profiles (id, name, initials, color, photo_url, is_admin)
  values (
    new.id, uname,
    coalesce(new.raw_user_meta_data->>'initials', upper(left(uname, 1))),
    coalesce(new.raw_user_meta_data->>'color', '#8b6f47'),
    new.raw_user_meta_data->>'photo_url',
    lower(split_part(new.email,'@',1)) = 'miguel'
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

-- ── Helpers de sessão / permissões ───────────────────────────────────────────
create or replace function public.session_owner(p_token text)
returns uuid language plpgsql security definer set search_path to 'public' as $$
declare uid uuid;
begin
  if p_token is null or length(p_token) < 20 then return null; end if;
  select profile_id into uid from public.sessions
   where token = p_token and expires_at > now();
  if uid is not null then
    update public.sessions set last_seen = now() where token = p_token;
  end if;
  return uid;
end $$;

create or replace function public.session_valid(p_token text)
returns boolean language sql security definer set search_path to 'public' as $$
  select exists (select 1 from public.sessions
                  where token = p_token and expires_at > now())
$$;

create or replace function public.is_admin(p_profile_id uuid)
returns boolean language sql security definer set search_path to 'public' as $$
  select coalesce((select is_admin from public.profiles where id = p_profile_id), false)
$$;

create or replace function public._edition_can_write(p_uid uuid, p_field text)
returns boolean language sql immutable as $$
