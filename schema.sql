-- ── Autenticação (com o fix do gen_random_bytes aplicado) ────────────────────
create or replace function public.auth_login2(p_profile_id uuid, p_hash text)
returns jsonb language plpgsql security definer set search_path to 'public' as $$
declare tok text; rec record;
begin
  perform pg_sleep(0.4);
  select pass_hash, must_change, temp_expires into rec
    from public.profiles_auth where profile_id = p_profile_id;
  if rec.pass_hash is null or rec.pass_hash <> p_hash then
    return jsonb_build_object('ok', false);
  end if;
  if rec.must_change and rec.temp_expires is not null and rec.temp_expires < now() then
    return jsonb_build_object('ok', false, 'expired', true);
  end if;

  delete from public.sessions
   where profile_id = p_profile_id
     and token not in (select token from public.sessions
                        where profile_id = p_profile_id order by last_seen desc limit 4);
  tok := encode(extensions.gen_random_bytes(32), 'hex');
  insert into public.sessions(token, profile_id) values (tok, p_profile_id);
  return jsonb_build_object('ok', true, 'token', tok, 'must_change', coalesce(rec.must_change, false));
end $$;

create or replace function public.auth_login(p_profile_id uuid, p_hash text)
returns text language plpgsql security definer set search_path to 'public' as $$
declare tok text;
begin
  perform pg_sleep(0.4);
  if not exists (select 1 from public.profiles_auth
                 where profile_id = p_profile_id and pass_hash = p_hash) then
    return null;
  end if;
  delete from public.sessions
   where profile_id = p_profile_id
     and token not in (select token from public.sessions
                        where profile_id = p_profile_id order by last_seen desc limit 4);
  tok := encode(extensions.gen_random_bytes(32), 'hex');
  insert into public.sessions(token, profile_id) values (tok, p_profile_id);
  return tok;
end $$;

create or replace function public.auth_verify(p_profile_id uuid, p_hash text)
returns boolean language plpgsql security definer set search_path to 'public' as $$
begin
  perform pg_sleep(0.4);
  return exists (select 1 from public.profiles_auth
                 where profile_id = p_profile_id and pass_hash = p_hash);
end $$;

create or replace function public.auth_register(p_profile_id uuid, p_hash text)
returns text language plpgsql security definer set search_path to 'public' as $$
declare tok text;
begin
  if exists (select 1 from public.profiles_auth where profile_id = p_profile_id) then
    raise exception 'credencial já existe';
  end if;
  insert into public.profiles_auth(profile_id, pass_hash) values (p_profile_id, p_hash);
  tok := encode(extensions.gen_random_bytes(32), 'hex');
  insert into public.sessions(token, profile_id) values (tok, p_profile_id);
  return tok;
end $$;

create or replace function public.auth_logout(p_token text)
returns void language sql security definer set search_path to 'public' as $$
  delete from public.sessions where token = p_token
$$;

create or replace function public.password_change(p_token text, p_old_hash text, p_new_hash text)
returns boolean language plpgsql security definer set search_path to 'public' as $$
declare uid uuid; rec record;
begin
  uid := public.session_owner(p_token);
  if uid is null then raise exception 'sessão inválida'; end if;
  if p_new_hash is null or length(p_new_hash) < 32 then raise exception 'password inválida'; end if;

  select pass_hash, must_change into rec from public.profiles_auth where profile_id = uid;
  if not coalesce(rec.must_change, false) then
    perform pg_sleep(0.3);
    if rec.pass_hash is distinct from p_old_hash then return false; end if;
  end if;

  update public.profiles_auth
     set pass_hash = p_new_hash, must_change = false, temp_expires = null, updated_at = now()
   where profile_id = uid;

  delete from public.sessions where profile_id = uid and token <> p_token;
  return true;
end $$;

create or replace function public.password_admin_reset(p_target_id uuid, p_temp_hash text, p_token text)
returns boolean language plpgsql security definer set search_path to 'public' as $$
declare caller uuid;
begin
  caller := public.session_owner(p_token);
  if caller is null then raise exception 'sessão inválida'; end if;
  if not public.is_admin(caller) then return false; end if;
  update public.profiles_auth
     set pass_hash = p_temp_hash, must_change = true, temp_expires = now() + interval '24 hours'
   where profile_id = p_target_id;
  delete from public.sessions where profile_id = p_target_id;
  return true;
end $$;

