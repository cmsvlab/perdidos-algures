-- ── Chat ─────────────────────────────────────────────────────────────────────
create or replace function public.chat_send(p_token text, p_text text)
returns public.chat_messages language plpgsql security definer set search_path to 'public' as $$
declare uid uuid; row public.chat_messages; recent int;
begin
  uid := public.session_owner(p_token);
  if uid is null then raise exception 'sessão inválida'; end if;
  if p_text is null or char_length(btrim(p_text)) = 0 or char_length(p_text) > 2000 then
    raise exception 'mensagem inválida'; end if;
  select count(*) into recent from public.chat_messages
   where profile_id = uid and created_at > now() - interval '1 minute';
  if recent >= 20 then raise exception 'demasiadas mensagens — abranda um pouco'; end if;

  insert into public.chat_messages(profile_id, text) values (uid, p_text) returning * into row;
  return row;
end $$;

create or replace function public.chat_delete(p_message_id bigint, p_token text)
returns boolean language plpgsql security definer set search_path to 'public' as $$
declare uid uuid; owner uuid;
begin
  uid := public.session_owner(p_token);
  if uid is null then raise exception 'sessão inválida'; end if;
  select profile_id into owner from public.chat_messages where id = p_message_id;
  if owner is distinct from uid and not public.is_admin(uid) then return false; end if;
  delete from public.chat_messages where id = p_message_id;
  return true;
end $$;

-- ── Bug reports ──────────────────────────────────────────────────────────────
create or replace function public.bug_report_send(p_token text, p_description text, p_image text default null)
returns boolean language plpgsql security definer set search_path to 'public' as $$
declare uid uuid; nome text; recent int;
begin
  uid := public.session_owner(p_token);
  if uid is null then raise exception 'sessão inválida'; end if;
  if char_length(coalesce(p_description,'')) not between 1 and 800 then
    raise exception 'descrição inválida'; end if;
  if p_image is not null and char_length(p_image) > 500000 then
    raise exception 'imagem demasiado grande'; end if;
  select count(*) into recent from public.bug_reports
   where by_id = uid::text and created_at > now() - interval '1 hour';
  if recent >= 5 then raise exception 'demasiados reports — tenta daqui a bocado'; end if;

  select coalesce(display_name, name) into nome from public.profiles where id = uid;
  insert into public.bug_reports(by_id, by_name, description, image)
  values (uid::text, nome, p_description, p_image);
  return true;
end $$;

create or replace function public.bug_reports_list(p_token text)
returns setof public.bug_reports language plpgsql security definer set search_path to 'public' as $$
declare uid uuid;
begin
  uid := public.session_owner(p_token);
  if uid is null or not public.is_admin(uid) then return; end if;
  return query select * from public.bug_reports order by created_at desc;
end $$;

create or replace function public.bug_reports_clear(p_token text, p_report_id uuid default null)
returns integer language plpgsql security definer set search_path to 'public' as $$
declare uid uuid; n integer;
begin
  uid := public.session_owner(p_token);
  if uid is null then raise exception 'sessão inválida'; end if;
  if not public.is_admin(uid) then return -1; end if;
  if p_report_id is null then delete from public.bug_reports; get diagnostics n = row_count;
  else delete from public.bug_reports where id = p_report_id; get diagnostics n = row_count; end if;
  return n;
end $$;

